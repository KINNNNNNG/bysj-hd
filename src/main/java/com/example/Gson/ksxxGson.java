package com.example.Gson;

import java.util.List;

public class ksxxGson {
    private String name;
    private String ksxz;
    private String type;
    private String kl;
    private List<Integer> ks;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKsxz() {
        return ksxz;
    }

    public void setKsxz(String ksxz) {
        this.ksxz = ksxz;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getKl() {
        return kl;
    }

    public void setKl(String kl) {
        this.kl = kl;
    }

    public List<Integer> getKs() {
        return ks;
    }

    public void setKs(List<Integer> ks) {
        this.ks = ks;
    }
}
